package com.emtap.pg270.formeval;

import java.lang.reflect.InvocationTargetException;
import java.lang.reflect.Method;


class CompatWrapper  {
	static void writeClipBoard (String sClipLabel, String sClipData, Object oSystemService) {
		try {
			Class<?> clClipboardManager = Class.forName("android.text.ClipboardManager");
			Object oClipBoardManager = clClipboardManager.cast(oSystemService);
			Method oMethod = clClipboardManager.getMethod("setText", CharSequence.class);
			oMethod.invoke(clClipboardManager.cast(oClipBoardManager), (CharSequence)sClipData);
		} catch (ClassNotFoundException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (NoSuchMethodException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (IllegalAccessException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (IllegalArgumentException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (InvocationTargetException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}
}
